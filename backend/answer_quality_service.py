def build_answer_quality_context(
    message: str,
    intent: str = "",
    intent_label: str = "",
    domain: str = "auto",
    allow_company_reference: bool = False,
) -> str:
    message = message or ""
    hints = detect_domain_hints(message)

    detected_summary = f"""
تشخیص‌های کمکی از متن کاربر:
- ماتریس‌های احتمالی: {", ".join(hints["matrices"]) if hints["matrices"] else "نامشخص"}
- آنالیت‌ها/پارامترهای احتمالی: {", ".join(hints["analytes"]) if hints["analytes"] else "نامشخص"}
- تکنیک‌های احتمالی: {", ".join(hints["techniques"]) if hints["techniques"] else "نامشخص"}
- نوع مسئله احتمالی: {", ".join(hints["issues"]) if hints["issues"] else "نامشخص"}
- حوزه انتخابی/تشخیصی اپ: {domain or "auto"}
""".strip()
    if allow_company_reference:
        company_reference_rule = """
قانون نام شرکت و لینک:
- چون کاربر مستقیماً درباره شرکت، سایت، تماس، خرید، نمایندگی یا اطلاعات تجاری پرسیده، اشاره محدود و حرفه‌ای به اطلاعات شرکت مجاز است.
- حتی در این حالت هم پاسخ را تبلیغاتی ننویس؛ اول پاسخ فنی بده، بعد مسیر تماس یا لینک را کوتاه مطرح کن.
""".strip()
    else:
        company_reference_rule = """
قانون نام شرکت و لینک:
- در پاسخ نهایی هیچ نام شرکت، لینک سایت، ایمیل، شماره تماس، واتساپ، عبارت «کارشناسان ما»، «شرکت ما» یا ارجاع تجاری نیاور.
- فقط پاسخ فنی، بی‌طرف و تخصصی بده.
- اگر سؤال درباره قیمت، خرید یا استعلام بود اما کاربر نام شرکت را نپرسیده، فقط بگو برای استعلام باید مشخصات محصول، تعداد، کاربرد و اطلاعات تماس ارائه شود؛ ایمیل یا نام شرکت نده.
""".strip()
    return f"""
قواعد کیفیت پاسخ حرفه‌ای آرتین:

{detected_summary}
{company_reference_rule}
{build_core_quality_rules()}
{build_presentation_rules()}
{build_intent_quality_rules(intent=intent, intent_label=intent_label)}

{build_matrix_quality_rules(hints)}

{build_final_answer_shape(intent)}

قانون نهایی:
- پاسخ نهایی را مثل یک کارشناس فنی باتجربه بنویس.
- از پاسخ خیلی کلی، فهرست بی‌هدف، ادعای ساختگی و پیشنهاد روش نامرتبط پرهیز کن.
- به کاربر نگو این قواعد از سیستم یا context آمده است.
""".strip()
