def build_matrix_quality_rules(hints: Dict[str, List[str]]) -> str:
    matrices = hints.get("matrices", [])
    analytes = hints.get("analytes", [])
    techniques = hints.get("techniques", [])

    blocks = []

    if "نمونه فرار یا تحت فشار" in matrices:
        blocks.append("""
قواعد نمونه‌های فرار، گازی یا تحت فشار:
- نمونه‌برداری، ظرف، فشار، دما، نشتی، dead volume، تبخیر و representative بودن نمونه را حتماً بررسی کن.
- روش‌های مخصوص مایع معمولی، cup باز یا نمونه غیر فشاری را به‌عنوان گزینه اصلی پیشنهاد نده، مگر روش/پیکربندی معتبر برای نمونه فرار یا تحت فشار وجود داشته باشد.
- برای LPG و گاز، ASTM D1265، D3700 یا روش‌های نمونه‌برداری معتبر را به‌عنوان بخش مهم کار مطرح کن، اما فقط وقتی مرتبط است.
- برای H2S، مرکاپتان، COS و CS2، جذب سطحی و ناپایداری ترکیبات گوگردی را در نظر بگیر.
- هشدار ایمنی درباره اشتعال‌پذیری، فشار و گازهای سمی لازم است.
""")

    if "آب و پساب" in matrices or "خاک و محیط زیست" in matrices:
        blocks.append("""
قواعد نمونه‌های آب، پساب، خاک و محیط زیست:
- نگهداری نمونه، ظرف، pH، دما، زمان نگهداری و آلودگی ثانویه مهم است.
- برای VOC/BTEX، headspace و از دست رفتن ترکیبات فرار را جدی بگیر.
- برای فلزات، digestion، acid preservation، blank، spike، CRM و matrix effect را بررسی کن.
- واحد گزارش باید متناسب با ماتریس باشد؛ مثل µg/L، mg/L، mg/kg.
""")

    if "نفت خام و میعانات" in matrices:
        blocks.append("""
قواعد نفت خام و میعانات:
- همگن‌سازی، دما، wax، آب آزاد، رسوب، گاز حل‌شده و نمونه چندفازی می‌تواند نتیجه را تغییر دهد.
- برای آب، نمک، رسوب، فلزات و گوگرد، sample handling و representative بودن test portion حیاتی است.
- اگر نمونه میعانات یا فرار است، loss اجزای سبک را در نظر بگیر.
""")

    if "سوخت مایع" in matrices:
        blocks.append("""
قواعد سوخت‌های مایع:
- مشخص کن هدف کنترل کیفیت، انطباق specification، عیب‌یابی فرایندی یا انتخاب دستگاه است.
- برای بنزین، دیزل، سوخت جت و سوخت دریایی، روش‌های آزمون و specification یکی نیستند.
- برای سوخت‌های کم‌گوگرد یا trace، LOD/LOQ و blank اهمیت دارد.
""")

    if "کاتالیست و جاذب" in matrices:
        blocks.append("""
قواعد کاتالیست و جاذب:
- برای انتخاب یا عیب‌یابی، فقط یک تست کافی نیست؛ معمولاً BET، XRD، TPR/TPD، SEM، ICP، XRF، crush strength یا attrition با توجه به مسئله لازم می‌شود.
- برای افت فعالیت، poisoning، coking، sintering، fouling، تغییر فاز، افت سطح ویژه، انسداد حفرات، آلودگی خوراک و شرایط عملیاتی را بررسی کن.
- اگر داده عملکردی وجود دارد، conversion، selectivity، yield، pressure drop و time-on-stream را تفسیر کن.
""")

    if "گوگرد کل" in analytes:
        blocks.append("""
قواعد گوگرد کل:
- Total sulfur با گونه‌بندی گوگردی فرق دارد.
- اگر کاربر H2S، مرکاپتان، COS یا CS2 می‌خواهد، روش Total sulfur به‌تنهایی کافی نیست.
- برای انتخاب روش، ماتریس، محدوده غلظت، LOD/LOQ و استاندارد قراردادی را بررسی کن.
""")

    if "گونه‌های گوگردی" in analytes:
        blocks.append("""
قواعد گونه‌بندی گوگردی:
- H2S، مرکاپتان‌ها، COS، CS2 و sulfides رفتار نمونه‌برداری متفاوت دارند.
- روش‌های GC با دتکتور انتخابی گوگرد یا روش‌های اختصاصی معمولاً مناسب‌تر از Total sulfur هستند.
- جذب سطحی، واکنش‌پذیری و ناپایداری ترکیبات گوگردی را توضیح بده.
""")

    if (
        "فلزات و عناصر" in analytes
        or "ICP/AAS" in techniques
        or "XRF/XRD" in techniques
    ):
        blocks.append("""
قواعد فلزات و عناصر:
- انتخاب بین XRF، ICP-OES، ICP-MS و AAS به ماتریس، غلظت، LOD/LOQ، تعداد عناصر، آماده‌سازی نمونه و هدف آزمون بستگی دارد.
- XRF معمولاً سریع‌تر و کم‌تخریب‌تر است، اما اثر ماتریس، ضخامت نمونه، یکنواختی، cup/film و کالیبراسیون می‌تواند خطای جدی ایجاد کند.
- ICP معمولاً برای trace metals و دقت بالاتر مناسب‌تر است، اما نیاز به digestion/رقیق‌سازی معتبر دارد.
- digestion ناقص در ICP می‌تواند فلزات را کم‌نمایی کند، مخصوصاً در نفت خام، fuel oil، کاتالیست مصرف‌شده یا نمونه‌های دارای ذرات.
- برای QC فلزات، blank، CRM، matrix spike، duplicate، calibration verification، internal standard و interference correction را بررسی کن.
- XRF برای screening سریع و غلظت‌های بالاتر مناسب‌تر است؛ ICP-MS برای غلظت‌های خیلی پایین و ICP-OES برای چندعنصری روتین مناسب‌تر است.
""")

    if "GC/GC-MS" in techniques or "HPLC" in techniques:
        blocks.append("""
قواعد کروماتوگرافی:
- برای GC/HPLC، baseline، noise، drift، peak shape، tailing/fronting، retention time، resolution، co-elution، carryover و calibration را بررسی کن.
- مشکلات را بین sample preparation، injection، column، mobile/carrier gas، detector، temperature program، leak و software تفکیک کن.
- اگر baseline نوسان دارد، فقط یک علت نگو؛ چک‌لیست مرحله‌ای بده.
""")

    if "XRF/XRD" in techniques:
        blocks.append("""
قواعد XRF/XRD:
- XRF برای اندازه‌گیری عنصری است و XRD برای شناسایی فاز کریستالی؛ این دو را قاطی نکن.
- در XRF اثر ماتریس، ضخامت نمونه، cup/film، کالیبراسیون، drift و محدوده کاری مهم است.
- در XRD آماده‌سازی پودر، preferred orientation، amorphous phase و حد تشخیص فازها مهم است.
""")

    if "خوردگی و HSE" in analytes:
        blocks.append("""
قواعد خوردگی و HSE:
- pH، کلراید، H2S، CO2، دما، فشار، oxygen ingress، velocity، metallurgy و inhibitor را بررسی کن.
- اگر H2S یا فشار مطرح است، هشدار ایمنی بده.
- برای انتخاب ماده یا sour service، بدون داده سرویس و استاندارد معتبر حکم قطعی نده.
""")

    if not blocks:
        blocks.append("""
قواعد عمومی حوزه فنی:
- ابتدا ماتریس، آنالیت، هدف کار، محدوده غلظت، روش یا دستگاه موجود، QC و محدودیت‌ها را از متن استخراج کن.
- اگر سؤال کلی است، پاسخ را به تصمیم عملی تبدیل کن: چه کاری انجام شود، چه چیزی بررسی شود، چه داده‌ای کم است.
""")

    return "\n".join(block.strip() for block in blocks)


