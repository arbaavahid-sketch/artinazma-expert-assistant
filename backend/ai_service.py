def ask_expert_assistant(
    message: str,
    context: str = "",
    history: Optional[List[Dict[str, str]]] = None,
    domain: str = "auto",
    allow_web_search: bool = False,
) -> str:
    user_content = build_user_content(
        message=message,
        context=context,
        history=history,
        domain=domain,
        allow_web_search=allow_web_search,
    )

    tools = []

    web_enabled = (
        allow_web_search
        and os.getenv("OPENAI_WEB_SEARCH_ENABLED", "false").strip().lower() == "true"
    )

    if web_enabled:
        tools.append({"type": "web_search_preview"})

    request_payload = {
        "model": MODEL,
        "input": [
            {
                "role": "system",
                "content": SYSTEM_PROMPT,
            },
            {
                "role": "user",
                "content": user_content,
            },
        ],
        "max_output_tokens": int(os.getenv("OPENAI_MAX_OUTPUT_TOKENS", "3000")),
        "temperature": OPENAI_TEMPERATURE,
    }

    if tools:
        request_payload["tools"] = tools

    try:
        response = client.responses.create(**request_payload)
        return clean_ai_answer(response.output_text)

    except Exception as e:
        if tools:
            print("OpenAI web search failed, retrying without web search:", e)

            request_payload.pop("tools", None)
            response = client.responses.create(**request_payload)
            return clean_ai_answer(response.output_text)

        raise e


def prepare_image_for_ai(file_path: str) -> Tuple[str, str]:
    """
    تصویر را کوچک و فشرده می‌کند تا درخواست Vision سنگین نشود.
    خروجی: base64 و mime_type
    """
    with Image.open(file_path) as image:
        image = ImageOps.exif_transpose(image)

        if image.mode not in ["RGB", "L"]:
            image = image.convert("RGB")

        image.thumbnail((1280, 1280))

        buffer = io.BytesIO()
        image.save(buffer, format="JPEG", quality=75, optimize=True)

        encoded_image = base64.b64encode(buffer.getvalue()).decode("utf-8")

    return encoded_image, "image/jpeg"


